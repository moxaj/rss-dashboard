package rss_dashboard.common.model.dashboard;

public class Position {
	public int top;
	public int left;
	public int width;
	public int height;

	public Position() {

	}

	public Position(int top, int left, int width, int height) {
		this.top = top;
		this.left = left;
		this.width = width;
		this.height = height;
	}
}
